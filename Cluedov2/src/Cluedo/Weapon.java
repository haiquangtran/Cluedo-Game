package Cluedo;
/**
 * This class represents a Weapon type of card
 * in the Deck. 
 * 
 * @author Quang Tran, Crystal Johnston
 *
 */
public class Weapon implements Card{
	private String name;
	
	public Weapon(String name){
		this.name = name;
	}

	/**
	 * Returns the name of the Weapon. 
	 */
	public String toString(){
		return name;
	}
}
